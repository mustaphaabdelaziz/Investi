$(document).ready(function () {
  // display only the first tab and make other hidden (display:none)
   let tabcontent = document.getElementsByClassName("navigation-tabs");
   let subtabcontent = document.getElementsByClassName("navigation-sub-tabs");
   for (i = 1; i < tabcontent.length; i++) {
     tabcontent[i].style.display = "none";
   }
   for (i = 1; i < subtabcontent.length; i++) {
     subtabcontent[i].style.display = "none";
   }
});
function openTab(evt, tabName) {
  // Declare all variables
  let i, tabcontent, tablinks;

  // Get all elements with class="tabcontent" and hide them
  tabcontent = document.getElementsByClassName("navigation-tabs");
  for (i = 0; i < tabcontent.length; i++) {
    tabcontent[i].style.display = "none";
  }

  // Get all elements with class="tablinks" and remove the class "active"
  tablinks = document.getElementsByClassName("tablinks");
  for (i = 0; i < tablinks.length; i++) {
    tablinks[i].className = tablinks[i].className.replace("active-tab", "");
  }

  // Show the current tab, and add an "active" class to the button that opened the tab
  document.getElementById(tabName).style.display = "block";
  evt.currentTarget.className += " active-tab";
}
